
package AdminBean;

public class RestaurantBean {
    private int Restaurant_id;
    private int home_dev;
    private String type;
    private int Place_Id;

    /**
     * @return the Restaurant_id
     */
    public int getRestaurant_id() {
        return Restaurant_id;
    }

    /**
     * @param Restaurant_id the Restaurant_id to set
     */
    public void setRestaurant_id(int Restaurant_id) {
        this.Restaurant_id = Restaurant_id;
    }

    /**
     * @return the home_dev
     */
    public int getHome_dev() {
        return home_dev;
    }

    /**
     * @param home_dev the home_dev to set
     */
    public void setHome_dev(int home_dev) {
        this.home_dev = home_dev;
    }

    /**
     * @return the type
     */
    public String getType() {
        return type;
    }

    /**
     * @param type the type to set
     */
    public void setType(String type) {
        this.type = type;
    }

    /**
     * @return the Place_Id
     */
    public int getPlace_Id() {
        return Place_Id;
    }

    /**
     * @param Place_Id the Place_Id to set
     */
    public void setPlace_Id(int Place_Id) {
        this.Place_Id = Place_Id;
    }
    
}
